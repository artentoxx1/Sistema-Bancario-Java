package Banco.SistemaBancario;

public class Fecha {
    private int dia;
    private int mes;
    private int anio;
    public Fecha() {
        dia=0;
        mes=0;
        anio=0;
    }
    public Fecha(int dia, int mes, int anio) {
        this.dia = dia;
        this.mes = mes;
        this.anio = anio;
    }
}
